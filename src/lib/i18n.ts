const DICTS: Record<Lang, Dict> = { ko, en, zh, ja };
const KEY = "jetpipe.lang.v1";

function detectDefault(): Lang {
  try {
    const stored = localStorage.getItem(KEY);
    if (stored && DICTS[stored as Lang]) return stored as Lang;
  } catch {
    /* ignore */
  }
  const nav = (navigator.language || "ko").toLowerCase();
  if (nav.startsWith("en")) return "en";
  if (nav.startsWith("zh")) return "zh";
  if (nav.startsWith("ja")) return "ja";
  return "ko";
}

let current: Lang = detectDefault();
const listeners = new Set<() => void>();

export function getLang(): Lang {
  return current;
}

export function setLang(l: Lang) {
  current = l;
  try {
    localStorage.setItem(KEY, l);
  } catch {
    /* ignore */
  }
  listeners.forEach((fn) => fn());
}

export function t(key: keyof typeof ko): string {
  return DICTS[current][key] ?? ko[key] ?? (key as string);
}

function subscribe(fn: () => void) {
  listeners.add(fn);
  return () => {
    listeners.delete(fn);
  };
}

/** Hook that re-renders the component whenever the language changes. */
export function useT() {
  useSyncExternalStore(subscribe, getLang, getLang);
  return t;
}
